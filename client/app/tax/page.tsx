/* ================= HELPERS ================= */

const colorMap: any = {
  emerald: "text-emerald-400",
  cyan: "text-cyan-400",
  rose: "text-rose-400",
};

function Metric({ title, value, color }: any) {
  return (
    <div className="rounded-2xl bg-white/5 border border-white/10 p-6">
      <p className="text-sm text-slate-400">{title}</p>
      <p className={`text-3xl font-bold mt-2 ${colorMap[color]}`}>
        ‚Çπ{value}
      </p>
    </div>
  );
}

function Section({ title, children }: any) {
  return (
    <section className="space-y-4">
      <h2 className="text-2xl font-semibold">{title}</h2>
      {children}
    </section>
  );
}

function Table({ headers, rows }: any) {
  return (
    <div className="overflow-hidden rounded-xl border border-white/10">
      <table className="w-full text-sm">
        <thead className="bg-white/5 text-slate-400">
          <tr>
            {headers.map((h: string) => (
              <th key={h} className="px-4 py-3 text-left">{h}</th>
            ))}
          </tr>
        </thead>
        <tbody>
          {rows.map((row: string[], i: number) => (
            <tr key={i} className="border-t border-white/10">
              {row.map((cell, j) => (
                <td key={j} className="px-4 py-3">{cell}</td>
              ))}
            </tr>
          ))}
        </tbody>
      </table>
    </div>
  );
}

function Insight({ good, text }: any) {
  return (
    <div className={`rounded-xl p-4 border ${
      good ? "border-green-400/30 bg-green-400/10" : "border-red-400/30 bg-red-400/10"
    }`}>
      <p className="text-sm">{good ? "‚úÖ" : "‚ö†Ô∏è"} {text}</p>
    </div>
  );
}